package galleryComment;

public class GalleryComment {

	private int galleryCommentID;
	private int galleryID;
	private String userID;
	private String galleryComment;
	private String galleryCommentDate;
	private int galleryCommentAvailable;
	
	public int getGalleryCommentID() {
		return galleryCommentID;
	}
	public void setGalleryCommentID(int galleryCommentID) {
		this.galleryCommentID = galleryCommentID;
	}
	public int getGalleryID() {
		return galleryID;
	}
	public void setGalleryID(int galleryID) {
		this.galleryID = galleryID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getGalleryComment() {
		return galleryComment;
	}
	public void setGalleryComment(String galleryComment) {
		this.galleryComment = galleryComment;
	}
	public String getGalleryCommentDate() {
		return galleryCommentDate;
	}
	public void setGalleryCommentDate(String galleryCommentDate) {
		this.galleryCommentDate = galleryCommentDate;
	}
	public int getGalleryCommentAvailable() {
		return galleryCommentAvailable;
	}
	public void setGalleryCommentAvailable(int galleryCommentAvailable) {
		this.galleryCommentAvailable = galleryCommentAvailable;
	}
	
	
}
