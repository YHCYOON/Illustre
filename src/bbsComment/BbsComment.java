package bbsComment;

public class BbsComment {

	private int bbsID;
	private String userID;
	private int bbsCommentID;
	private String bbsComment;
	private String bbsCommentDate;
	private int bbsAvailable;
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getBbsCommentID() {
		return bbsCommentID;
	}
	public void setBbsCommentID(int bbsCommentID) {
		this.bbsCommentID = bbsCommentID;
	}
	public String getBbsComment() {
		return bbsComment;
	}
	public void setBbsComment(String bbsComment) {
		this.bbsComment = bbsComment;
	}
	public String getBbsCommentDate() {
		return bbsCommentDate;
	}
	public void setBbsCommentDate(String bbsCommentDate) {
		this.bbsCommentDate = bbsCommentDate;
	}
	public int getBbsAvailable() {
		return bbsAvailable;
	}
	public void setBbsAvailable(int bbsAvailable) {
		this.bbsAvailable = bbsAvailable;
	}
	
	
}
