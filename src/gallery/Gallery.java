package gallery;

public class Gallery {
	
	private int galleryID;
	private String userID;
	private String userNickname;
	private String galleryCategory;
	private String galleryTitle;
	private String galleryContent;
	private String galleryDate;
	private String fileName;
	private String fileRealName;
	private int galleryLikeCount;
	private int galleryAvailable;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getGalleryID() {
		return galleryID;
	}
	public void setGalleryID(int galleryID) {
		this.galleryID = galleryID;
	}
	public String getUserNickname() {
		return userNickname;
	}
	public void setUserNickname(String userNickname) {
		this.userNickname = userNickname;
	}
	public String getGalleryCategory() {
		return galleryCategory;
	}
	public void setGalleryCategory(String galleryCategory) {
		this.galleryCategory = galleryCategory;
	}
	public String getGalleryTitle() {
		return galleryTitle;
	}
	public void setGalleryTitle(String galleryTitle) {
		this.galleryTitle = galleryTitle;
	}
	public String getGalleryContent() {
		return galleryContent;
	}
	public void setGalleryContent(String galleryContent) {
		this.galleryContent = galleryContent;
	}
	public String getGalleryDate() {
		return galleryDate;
	}
	public void setGalleryDate(String galleryDate) {
		this.galleryDate = galleryDate;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileRealName() {
		return fileRealName;
	}
	public void setFileRealName(String fileRealName) {
		this.fileRealName = fileRealName;
	}
	public int getGalleryLikeCount() {
		return galleryLikeCount;
	}
	public void setGalleryLikeCount(int galleryLikeCount) {
		this.galleryLikeCount = galleryLikeCount;
	}
	public int getGalleryAvailable() {
		return galleryAvailable;
	}
	public void setGalleryAvailable(int galleryAvailable) {
		this.galleryAvailable = galleryAvailable;
	}

	
}
